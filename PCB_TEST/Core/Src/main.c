void FOC_SetPWM_Hardware(float dutyA, float dutyB, float dutyC) {
    TIM1->CCR1 = (uint16_t)(dutyA * PWM_PERIOD_ARR);
    TIM1->CCR2 = (uint16_t)(dutyB * PWM_PERIOD_ARR);
    TIM1->CCR3 = (uint16_t)(dutyC * PWM_PERIOD_ARR);
}
void Calibrate_ADC_Offset(void) {
    uint32_t sum_A = 0;
    uint32_t sum_B = 0;

    // 0. BẬT NGUỒN KHỐI ADC LÊN TRƯỚC (QUAN TRỌNG NHẤT!)
    __HAL_ADC_ENABLE(&hadc1);

    // Chờ 1 chút xíu (khoảng vài micro giây) để điện áp lõi ADC ổn định
    for(volatile int wait = 0; wait < 10000; wait++);

    // 1. Lưu lại cấu hình Trigger hiện tại của CubeMX
    uint32_t old_cr2 = hadc1.Instance->CR2;

    // 2. Tắt External Trigger để ép ADC đọc thủ công bằng phần mềm
    hadc1.Instance->CR2 &= ~ADC_CR2_JEXTEN;

    // 3. Đọc 1000 mẫu siêu tốc
    for(int i = 0; i < 1000; i++) {
        hadc1.Instance->CR2 |= ADC_CR2_JSWSTART;        // Bóp cò bằng phần mềm
        while((hadc1.Instance->SR & ADC_SR_JEOC) == 0); // CHẮC CHẮN KHÔNG CÒN BỊ TREO Ở ĐÂY NỮA
        hadc1.Instance->SR &= ~ADC_SR_JEOC;             // Xóa cờ hoàn thành

        sum_A += hadc1.Instance->JDR1;
        sum_B += hadc1.Instance->JDR2;
    }

    // 4. Chia trung bình để lấy Offset thực tế
    offset_A = sum_A / 1000;
    offset_B = sum_B / 1000;

    // 5. Trả lại cấu hình Trigger phần cứng từ Timer 1
    hadc1.Instance->CR2 = old_cr2;
}

void HAL_ADCEx_InjectedConvCpltCallback(ADC_HandleTypeDef* hadc) {
    if (hadc->Instance == ADC1) {
        uint16_t raw_ADC_A = hadc->Instance->JDR1;
        uint16_t raw_ADC_B = hadc->Instance->JDR2;
        int16_t delta_A = raw_ADC_A - offset_A;
        int16_t delta_B = raw_ADC_B - offset_B;
        Ia = (float)delta_A * ADC_TO_AMPS_FACTOR;
        Ib = (float)delta_B * ADC_TO_AMPS_FACTOR;
        Ic = -Ia - Ib;
        motor1.Ia = Ia;
        motor1.Ib = Ib;
        motor1.Ic = Ic;
    }
}

