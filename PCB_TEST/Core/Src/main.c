uint32_t PWM_Width = 0;
uint8_t Is_First_Captured = 0;

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM2 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_4)
    {
        if (Is_First_Captured == 0)
        {
            IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_4);
            Is_First_Captured = 1;
            __HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_4, TIM_INPUTCHANNELPOLARITY_FALLING);
        }
        else if (Is_First_Captured == 1)
        {
            IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_4);
            if (IC_Val2 > IC_Val1) {
                PWM_Width = IC_Val2 - IC_Val1;
            } else if (IC_Val1 > IC_Val2) {
                PWM_Width = (0xFFFFFFFF - IC_Val1) + IC_Val2 + 1;
            }

            if (PWM_Width < 900 || PWM_Width > 2100) {
                motor1.target_RPM = 0.0f;
            }
            else {
                if (PWM_Width < 1050) PWM_Width = 1000;
                if (PWM_Width > 1950) PWM_Width = 2000;
                motor1.target_RPM = (float)(PWM_Width - 1000) * (8000.0f / 1000.0f);
            }
            Is_First_Captured = 0;
            __HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_4, TIM_INPUTCHANNELPOLARITY_RISING);
        }
    }
}

